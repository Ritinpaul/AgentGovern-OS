"""
HTML Report Generator — produces beautiful HTML report for sharing and archiving.

Generates a self-contained HTML file with:
- Executive summary with metrics
- Agent inventory table
- Policy violations with severity badges
- Framework detection results
- Scan metadata
"""

from __future__ import annotations

import json
from datetime import datetime
from pathlib import Path
from typing import Any, TYPE_CHECKING

if TYPE_CHECKING:
    from agentgovern.policy.engine import PolicyCheckResult, PolicyViolation
    from agentgovern.scanner.codeprint import CodeprintScanResult

SEVERITY_COLORS = {
    "CRITICAL": "#dc2626",  # red-600
    "HIGH": "#ea580c",      # orange-600
    "MEDIUM": "#ca8a04",    # yellow-600
    "LOW": "#16a34a",       # green-600
}

RISK_COLORS = {
    "CRITICAL": "#dc2626",
    "HIGH": "#ea580c",
    "MEDIUM": "#ca8a04",
    "LOW": "#16a34a",
}


def build_html_report(
    abom: dict[str, Any],
    policy_result: "PolicyCheckResult",
    codeprint_result: "CodeprintScanResult",
) -> str:
    """
    Build a self-contained HTML report from ABOM and scan results.

    Args:
        abom: Agent Bill of Materials dictionary
        policy_result: Policy check results
        codeprint_result: Code scanning results

    Returns:
        Complete HTML document as string
    """
    summary = abom.get("summary", {})
    agents = abom.get("agents", [])
    frameworks = abom.get("frameworks_detected", [])
    metadata = abom.get("metadata", {})

    # Build executive summary
    exec_summary = _build_executive_summary(summary)

    # Build agents table
    agents_table = _build_agents_table(agents)

    # Build violations section
    violations_section = _build_violations_section(policy_result)

    # Build frameworks section
    frameworks_section = _build_frameworks_section(frameworks)

    # Build secrets warning
    secrets_section = _build_secrets_section(codeprint_result)

    # Build metadata footer
    metadata_footer = _build_metadata_footer(metadata)

    # Assemble full HTML
    html = f"""<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <meta name="generator" content="AgentGovern OS">
    <title>AgentGovern Scan Report — {metadata.get('project_name', 'Project')}</title>
    <style>
        {_get_css()}
    </style>
</head>
<body>
    <div class="container">
        <header class="header">
            <h1>🛡️ AgentGovern OS</h1>
            <p class="subtitle">AI Agent Governance Scan Report</p>
        </header>

        {exec_summary}
        {agents_table}
        {violations_section}
        {secrets_section}
        {frameworks_section}
        {metadata_footer}

        <footer class="footer">
            <p>Generated by <a href="https://github.com/Ritinpaul/AgentGovern-OS" target="_blank">AgentGovern OS</a> — The open-source governance scanner for AI agents</p>
        </footer>
    </div>
</body>
</html>
"""
    return html


def _build_executive_summary(summary: dict[str, Any]) -> str:
    """Build the executive summary section."""
    passed = summary.get("overall_pass", False)
    status_class = "status-pass" if passed else "status-fail"
    status_text = "✓ PASSED" if passed else "✗ FAILED"

    critical = summary.get("critical", 0)
    high = summary.get("high", 0)
    medium = summary.get("medium", 0)
    low = summary.get("low", 0)
    secrets = summary.get("hardcoded_secrets", 0)
    total_agents = summary.get("total_agents", 0)

    return f"""
    <section class="executive-summary">
        <h2>Executive Summary</h2>
        <div class="status-badge {status_class}">{status_text}</div>

        <div class="metrics-grid">
            <div class="metric-card">
                <div class="metric-value">{total_agents}</div>
                <div class="metric-label">Total Agents</div>
            </div>
            <div class="metric-card severity-critical">
                <div class="metric-value">{critical}</div>
                <div class="metric-label">Critical</div>
            </div>
            <div class="metric-card severity-high">
                <div class="metric-value">{high}</div>
                <div class="metric-label">High</div>
            </div>
            <div class="metric-card severity-medium">
                <div class="metric-value">{medium}</div>
                <div class="metric-label">Medium</div>
            </div>
            <div class="metric-card severity-low">
                <div class="metric-value">{low}</div>
                <div class="metric-label">Low</div>
            </div>
            <div class="metric-card severity-critical">
                <div class="metric-value">{secrets}</div>
                <div class="metric-label">Hardcoded Secrets</div>
            </div>
        </div>
    </section>
    """


def _build_agents_table(agents: list[dict[str, Any]]) -> str:
    """Build the agents inventory table."""
    if not agents:
        return '<section><h2>🤖 Detected Agents</h2><p class="warning">No agents found in manifest.</p></section>'

    rows = []
    for agent in agents:
        code = agent.get("code", "")
        name = agent.get("name", "")
        framework = agent.get("framework", "—")
        tier = agent.get("tier") or "unset"
        authority = agent.get("authority_limit")
        currency = agent.get("currency", "USD")
        risk = agent.get("risk_level", "LOW")

        authority_str = f"{authority:,.2f} {currency}" if authority is not None else "unset"
        risk_color = RISK_COLORS.get(risk, "#6b7280")

        rows.append(f"""
        <tr>
            <td class="code-cell">{code}</td>
            <td>{name}</td>
            <td class="framework-cell">{framework}</td>
            <td class="tier-cell">{tier}</td>
            <td class="authority-cell">{authority_str}</td>
            <td><span class="risk-badge" style="background-color: {risk_color};">{risk}</span></td>
        </tr>
        """)

    return f"""
    <section>
        <h2>🤖 Detected Agents</h2>
        <table class="agents-table">
            <thead>
                <tr>
                    <th>Code</th>
                    <th>Name</th>
                    <th>Framework</th>
                    <th>Tier</th>
                    <th>Authority Limit</th>
                    <th>Risk</th>
                </tr>
            </thead>
            <tbody>
                {"".join(rows)}
            </tbody>
        </table>
    </section>
    """


def _build_violations_section(policy_result: "PolicyCheckResult") -> str:
    """Build the policy violations section."""
    if not policy_result.violations:
        return '<section><h2>⚠️ Policy Violations</h2><p class="success">✅ No policy violations found!</p></section>'

    rows = []
    for v in policy_result.violations:
        sev_color = SEVERITY_COLORS.get(v.severity, "#6b7280")
        rows.append(f"""
        <tr>
            <td class="rule-id-cell">{v.rule_id}</td>
            <td><span class="severity-badge" style="background-color: {sev_color};">{v.severity}</span></td>
            <td class="agent-cell">{v.agent_code or "project"}</td>
            <td>{v.message}</td>
            <td class="suggestion-cell">{v.suggestion}</td>
        </tr>
        """)

    return f"""
    <section>
        <h2>⚠️ Policy Violations</h2>
        <table class="violations-table">
            <thead>
                <tr>
                    <th>Rule ID</th>
                    <th>Severity</th>
                    <th>Agent</th>
                    <th>Message</th>
                    <th>Suggestion</th>
                </tr>
            </thead>
            <tbody>
                {"".join(rows)}
            </tbody>
        </table>
    </section>
    """


def _build_secrets_section(codeprint_result: "CodeprintScanResult") -> str:
    """Build the hardcoded secrets warning section."""
    if not codeprint_result.secret_detections:
        return ""

    items = []
    for s in codeprint_result.secret_detections:
        items.append(f"""
        <div class="secret-item">
            <div class="secret-type">{s.secret_type}</div>
            <div class="secret-location">{s.file}:{s.line}</div>
            <code class="secret-snippet">{s.snippet}</code>
        </div>
        """)

    return f"""
    <section class="secrets-section">
        <h2>🔑 Hardcoded Secrets Detected</h2>
        <div class="secrets-list">
            {"".join(items)}
        </div>
    </section>
    """


def _build_frameworks_section(frameworks: list[dict[str, Any]]) -> str:
    """Build the detected frameworks section."""
    if not frameworks:
        return ""

    items = []
    for fw in frameworks:
        confidence = "HIGH CONFIDENCE" if fw.get("high_confidence") else "detected"
        conf_class = "conf-high" if fw.get("high_confidence") else "conf-medium"
        items.append(f"""
        <div class="framework-item">
            <span class="framework-name">{fw.get('framework', '')}</span>
            <span class="framework-package">({fw.get('package', '')})</span>
            <span class="framework-source">via {fw.get('source_file', '')}</span>
            <span class="confidence-badge {conf_class}">{confidence}</span>
        </div>
        """)

    return f"""
    <section>
        <h2>📦 AI Frameworks Detected</h2>
        <div class="frameworks-list">
            {"".join(items)}
        </div>
    </section>
    """


def _build_metadata_footer(metadata: dict[str, Any]) -> str:
    """Build the scan metadata footer."""
    project = metadata.get("project_name", "Unknown")
    timestamp = metadata.get("scan_timestamp", datetime.now().isoformat())
    duration = metadata.get("scan_duration_s", 0)

    return f"""
    <section class="metadata">
        <h3>Scan Metadata</h3>
        <dl>
            <dt>Project:</dt><dd>{project}</dd>
            <dt>Scan Time:</dt><dd>{timestamp}</dd>
            <dt>Duration:</dt><dd>{duration:.2f}s</dd>
            <dt>Scanner Version:</dt><dd>{metadata.get('scanner_version', 'unknown')}</dd>
        </dl>
    </section>
    """


